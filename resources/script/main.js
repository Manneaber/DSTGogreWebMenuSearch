let items = [];
let itemsMap = new Map();

function renderItem(data) {
    for (let i = 0; i < data.length; i++) {
        let tmpString = `<div class="col-md-auto"><div class="food-item" id="${i}">`;
        tmpString += `<div class="${data[i].dish}"></div>`;
        tmpString += `<img class="food-img" src="${data[i].image}" width="80px" height="80px" draggable="false">`;
        tmpString += '<div class="mini-img-dtl">';

        let highCoin = data[i].coins[3] != 0 ? 4 : data[i].coins[2] != 0 ? 3 : data[i].coins[1] != 0 ? 2 : 1;
        let highPlateCoin = data[i].silver_coins[3] != 0 ? 4 : data[i].silver_coins[2] != 0 ? 3 : data[i].silver_coins[1] != 0 ? 2 : 1;
        tmpString += '<div class="coin coin-' + highCoin + '"></div>';
        tmpString += '<div class="coin coin-' + highPlateCoin + '"></div>';

        let cooker = '';
        switch (data[i].station[0]) {
            case 'pot': cooker = 1; break;
            case 'grill': cooker = 2; break;
            case 'oven': cooker = 3; break;
            default: cooker = 8; break;
        }

        tmpString += '<div class="cook cook-' + cooker + '"></div>';
        tmpString += '</div></div></div>';

        $('.food-panel').append(tmpString);
    }

    $('.food-item').bind('click', (event) => {
        showDetails(event.currentTarget.id);
    });
}

function toggleVisibleItem(list) {
    if (list !== null) {
        for (let i = 0; i < items.length; i++) {
            if (!list.includes(i)) {
                $(`#${i}.food-item`).parent().addClass('hidden');
            } else {
                $(`#${i}.food-item`).parent().removeClass('hidden');
            }
        }
    } else {
        $('.food-item').parent().removeClass('hidden');
    }
}

function showDetails(id) {
    $('.recipe-details-title').text(items[id].name);
    $('.recipe-details-dish').attr('class', 'recipe-details-dish ' + items[id].dish);
    $('.recipe-details-img').attr('src', items[id].image);

    let offering = '';
    for (let i = items[id].coins.length; i >= 0; i--) {
        if (items[id].coins[i] > 0) {
            offering += `${items[id].coins[i]}<span class="coin-inline coin-${i + 1}"></span> `;
        }
    }
    $('.recipe-details-coin').html('<strong>Offering</strong>: ' + offering);

    let offeringSilver = '';
    for (let i = items[id].silver_coins.length; i >= 0; i--) {
        if (items[id].silver_coins[i] > 0) {
            offeringSilver += `${items[id].silver_coins[i]}<span class="coin-inline coin-${i + 1}"></span> `;
        }
    }
    $('.recipe-details-scoin').html('<strong>Silver Dish Offering</strong>: ' + offeringSilver);
    $('.recipe-details-craving').html('<strong>Satisfies Craving</strong>: ' + items[id].cravings);
    $('.recipe-details-tools').html('<strong>Cooking Stations</strong>: ' + items[id].station[0]);

    let indeg = '';
    for (let i = 0; i < items[id].ingredients.length; i++) {
        indeg += '<div class="indeg-row">';
        for (let j = 0; j < items[id].ingredients[i].length; j++) {
            indeg += '<div class="indeg-col">';
            indeg += '<img src="https://s3.amazonaws.com/kleiforums/GORGE/RecipeBook/images/ingredients/' + items[id].ingredients[i][j] + '.png" width="32px" height="32px" draggable="false">';
            indeg += '</div>';
        }
        indeg += '</div>';
    }
    $('.recipe-details-indeg').html(indeg);
}

